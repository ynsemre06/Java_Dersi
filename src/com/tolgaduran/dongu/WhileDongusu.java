package com.tolgaduran.dongu;

public class WhileDongusu {
    public static void main(String[] args) {
        // while() Döngüsü
        int deger = 0;

        while (deger < 5) {
            System.out.println("Değer : " + deger);
            // deger = deger + 1;
            deger++;
        }
    }
}
